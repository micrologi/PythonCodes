# Operadores Matemáticos

# Soma +
print(-4 + 4)
print(-4. + 8)


# Subtração -


# Multiplicação *
print(2 * 3)
print(2 * 3.)
print(2. * 3)
print(2. * 3.)


# Divisão (Sempre retorna um float) /
print(6 / 3)
print(6 / 3.)
print(6. / 3)
print(6. / 3.)


# Divisão Arredondada (Divisão de inteiro para inteiro resulta em 1 inteiro, senão um float) //
print(6 // 3)
print(6 // 3.)
print(6. // 3)
print(6. // 3.)
print(-6 // 4)
print(6. // -4)


# Módulo (Resto da divisão) %
print(14 % 4)
print(12 % 4.5)


# Exponenciação **
print(2 ** 3)
print(2 ** 3.)
print(2. ** 3)
print(2. ** 3.)


#Ordem dos operadores
print(2 * 3 % 5)


#Parenteses
print((5 * ((25 % 13) + 100) / (2 * 13)) // 2)


#Qual é a saída esperada do trecho a seguir?
print((2 ** 4), (2 * 4.), (2 * 4))